parrot = "Norwegian Blue"
print(parrot[2]) # indexing
print(parrot[-2]) # negative indexing
print(parrot[0:6]) #slicing
print(parrot[-1:-8]) # negative slicing
print(parrot[0:6:2]) #slicing in steps
print(parrot[::2]) #slicing entire string in steps of two
print(parrot[::-1]) #negative slicing in steps of one
print(parrot[10:3:-1]) #negative slicing in give range in single steps